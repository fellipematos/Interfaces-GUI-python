#leia um numero inteiro mostra seu sucessor e antecessor

numero = int(input('Digite um numero: '))

print('O numero digitado foi {:^10}'.format(numero))
print('Seu sucessor é: {}'.format(numero+1))
print('Seu antecessor é: {}'.format(numero-1))

print(f'numero {numero} antecessor {numero-1} sucessor {numero+1}')
